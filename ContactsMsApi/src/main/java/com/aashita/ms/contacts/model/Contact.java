package com.aashita.ms.contacts.model;

import java.util.logging.Logger;

public class Contact {
	private String id = "foo";
	private String name="Mr. Foo";
	private String mobile = "000-000-0000";
	
	Logger logger = Logger.getLogger("com.aashita.ms.contacts.model.Contact");
	
	
	public Contact() {
		logger.fine("Contact() constructer called!!");
	};
	
	public Contact(String id, String nam, String mob) {
		this.id=id;
		this.name =nam;
		this.mobile=mob;
		logger.fine("Contact(id,n,m) constructer called!!");		
	}
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	
	
	
	
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		result = prime * result + ((mobile == null) ? 0 : mobile.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}
	
	
	
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Contact other = (Contact) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		if (mobile == null) {
			if (other.mobile != null)
				return false;
		} else if (!mobile.equals(other.mobile))
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}
	
	

}
